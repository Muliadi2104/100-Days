package natan;
public class natan {
    public static void main(String[] args) {
        
        int x = 9;
        int y = 3;
        
        //Operator penjumlahan
        int sum1 = x + y;
        System.out.println("Penjumlahan: " + sum1);
        
        // Operator pengurangan
        int sum2= x - y;
        System.out.println("Pengurangan: " + sum2);
        
        // Operator perkalian
        int sum3= x * y;
        System.out.println("Perkalian: " + sum3);
        
        // Operator pembagian
        int sum4 = x / y;
        System.out.println("Pembagian: " + sum4);
        
        // Operator sisa bagi
        int sum5 = x % y;
        System.out.println("Sisa bagi: " + sum5);

    }
}
