import java.util.*;

 class Dcoder
 {
   public static void main(String args[])
   { 
  

    Scanner natan = new Scanner (System.in);
    System.out.print("Masukkan nilai A = ");
    int nilaiA = natan.nextInt();
    
    if (nilaiA >= 6){
        System.out.println(nilaiA +" >="+ 6);
    }
    else if (nilaiA <= 6){
        System.out.println(nilaiA + " <= "+ 6);
    
    
}
  }
 }
